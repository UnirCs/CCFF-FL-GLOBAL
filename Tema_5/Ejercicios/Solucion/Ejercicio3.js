// Declaramos el precio original y el porcentaje de descuento
let precioOriginal = 100; // Ejemplo: 100 euros
let porcentajeDescuento = 20; // Ejemplo: 20%

// Calculamos el descuento
let descuento = (precioOriginal * porcentajeDescuento) / 100;

// Calculamos el precio final
let precioFinal = precioOriginal - descuento;

// Mostramos el precio final en la consola
console.log("El precio final con descuento es: " + precioFinal + " euros");